	OUTPUT = 'OUTPUT'
	INPUT = 'INPUT'
	DATA_DESC = 'DATA_DESC'
	CRS = 'CRS'
	EXPORT_CSV = 'EXPORT_CSV'
	CSV_OUTPUT_DIR = 'CSV_OUTPUT_DIR'
	CSV_OUTPUT_FOLDER_NAME = 'CSV_OUTPUT_FOLDER_NAME'
	CSV_APPEND_MODE = 'CSV_APPEND_MODE'

